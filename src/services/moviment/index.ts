export function checkNextMoveIsValid(canvasPosition: IPosition, sqm: number = 1) {
  if (sqm === 1) {
    return canvas[canvasPosition.y][canvasPosition.x] === 0;
  }

  for (let i = 0; i < sqm; i++) {
    const isValid = canvas[canvasPosition.y + 1][canvasPosition.x + i] === 0;
    if (!isValid) {
      return false;
    }
  }

  return true;
}

export function getNewPosition(direction: EDirections, position: IPosition): IPosition {
  switch (direction) {
    case EDirections.UP:
      return {
        x: position.x,
        y: position.y - 1,
      };

    case EDirections.RIGHT:
      return {
        x: position.x + 1,
        y: position.y,
      };

    case EDirections.DOWN:
      return {
        x: position.x,
        y: position.y + 1,
      };

    case EDirections.LEFT:
      return {
        x: position.x - 1,
        y: position.y,
      };

    default:
      return position;
  }
}
